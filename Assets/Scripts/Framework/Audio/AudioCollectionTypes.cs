namespace ProefExamen.Framework.Audio
{
    /// <summary>
    /// Enum for the different types of UI audio.
    /// </summary>
    public enum AudioCollectionTypes
    {
        /// <summary>
        /// Sound type for UI back buttons.
        /// </summary>
        Back,

        /// <summary>
        /// Sound type for UI select buttons.
        /// </summary>
        Select,

        /// <summary>
        /// Sound type for UI deny/uninteractable buttons.
        /// </summary>
        Denied,

        /// <summary>
        /// Sound type for UI confirm buttons.
        /// </summary>
        Confirm,
    }
}
